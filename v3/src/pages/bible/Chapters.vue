<template>
  <tool-bar></tool-bar>
  <v-main>
    <div class="bible-chapters-container">
      <div
        v-for="(chapter, chapterId) in chapters"
        v-bind:key="chapterId"
        class="bible-chapter"
        @click="goTo(chapter.c)"
      >
        {{ chapter.c }}
      </div>
    </div>
  </v-main>
</template>

<script lang="ts">
import chaptersData from "@/data/bible/chapters";
import ToolBar from "@/components/layout/ToolBar.vue";

export default {
  name: "bible-chapters",
  data() {
    return {
      chapters: [],
    };
  },
  methods: {
    goTo(link: string) {
      this.$router.push(`/bible/${this.$route.params.book}/${link}`);
    },
  },
  created() {
    this.chapters = chaptersData[this.$route.params.book];
  },
  components: { ToolBar },
};
</script>

